#include"fdf.h"

int	valuescount(char *s)
{
	int	i;
	int	count;

	count = 0;
	i = 0;
	while (s[i] != '\0')
	{
		while ((s[i] >= 48 && s[i] <= 57 || s[i] == '-') && s[i] != '\0')
			i++;
		while (!(s[i] >= 48 && s[i] <= 57 || s[i] == '-') && s[i] != '\0')
			i++;
		count++;
	}
	return (count);
}
